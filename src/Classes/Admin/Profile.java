package Classes.Admin;

public class Profile {
    private int age;

    private float height;

    private float weight;

    private float imc;

    private char gender;

    public void Profile(){}


    public void Profile(int age, float height, float weight, float imc, char gender){
        this.age = age;
        this.height = height;
        this.weight = weight;
        this.imc = imc;
        this.gender = gender;
    }



}
